/// **ThemeConstants Class**
///
/// A utility class containing constants related to themes.
///
/// Example Usage:
/// ```dart
/// ThemeConstants.sysRadio // Accessing the system theme mode constant
/// ```
class ThemeConstants {
  const ThemeConstants._();

  /// Represents the system theme mode constant.
  static const String sysRadio = 'ThemeMode.system';

  /// Represents the system theme mode constant.
  static const String lightRadio = 'ThemeMode.light';

  /// Represents the dark theme mode constant.

  static const String darkRadio = 'ThemeMode.dark';
}

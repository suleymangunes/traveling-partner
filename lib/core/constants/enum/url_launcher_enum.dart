/// **UrlLauncherEnum Enumeration**
///
/// An enumeration representing URL launcher parameters.
///
/// Example Usage:
/// ```dart
/// UrlLauncherEnum.mailto // Accessing the 'mailto' parameter
/// ``
enum UrlLauncherEnum {
  /// Represents the 'mailto' parameter.
  mailto,

  /// Represents the 'subject' parameter.
  subject,

  /// Represents the 'body' parameter.
  body,
}
